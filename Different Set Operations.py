#Write a Python Program to Illustrate Different Set Operations Here we have to define two set variables and we have to perform different set operations: union, intersection, difference and symmetric difference.
E = {0, 2, 4, 6, 8}
N = {1, 2, 3, 4, 5}

# set union
print("Union of E and N is=",E | N)

# set intersection
print("Intersection of E and N is=",E & N)

# set difference
print("Difference of E and N is=",E - N)

# set symmetric difference
print("Symmetric difference of E and N is=",E ^ N)
